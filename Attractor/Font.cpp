void drawtext(char *text){
	int i = 0;
	int count = 0;
	do{
		if (text[i] == '\n'){
			glTranslatef(-1.1*count, -1.1, 0.0);
			count = 0;
		}
		else{
			drawchar(text[i]);
			glTranslatef(1.1, 0.0, 0.0);
			count++;
		}
		i++;
	} while (text[i] != '\0');
}